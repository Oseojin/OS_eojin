; -----------------------------------------------------------------------------
;  Kernel Entry Point
;  부트로더가 32비트 보호모드로 전환 후, 0x1000 주소로 점프했을 때
;  가장 먼저 실행되는 코드입니다.
; -----------------------------------------------------------------------------

[bits 32]           ; 32비트 모드
[extern main]       ; kmain.c에 정의된 'main' 함수를 참조하겠다고 선언

global _start       ; 링커가 알 수 있도록 시작점 표시

_start:
    call main       ; C 언어의 main() 함수 호출
    jmp $           ; main에서 리턴되면 무한 루프 (시스템 멈춤)